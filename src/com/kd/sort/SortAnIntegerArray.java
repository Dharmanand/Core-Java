package com.kd.sort;

import java.util.Arrays;

public class SortAnIntegerArray {

	public static void main(String[] args) {
		
		int arr[] = {4, 6, 8, 2, 15, 25, 29, 84, 11};
		
		Arrays.sort(arr);
		
		
		
		System.out.println("1st Max : "+arr[arr.length-1]);
		System.out.println("1st Max : "+arr[arr.length-2]);

	}
	
	

}
